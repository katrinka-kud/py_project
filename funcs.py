number_1 = 10
number_2 = 6


def add_numbers() -> int:
    """
    Функция, которая складывает два числа
    :return:
    """
    sum_numbers = number_1 + number_2
    return sum_numbers


def number_difference() -> int:
    """
    Функция, реализующую вычитание
    :return:
    """
    difference = number_1 - number_2
    return difference
